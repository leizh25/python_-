a = 1
# a 加上一个2 打印结果
# a = a + 2
a += 2
print(a)

b = 1
# b乘一个3 打印结果
# b = b * 3
b *= 3
print(b)

c = 2
# c减1  打印结果
# c = c - 1
c -= 1
print(c)

d = 3
# d除以2 打印结果
# d = d / 2
d /= 2
print(d)

e = 3
# e 整除2 打印结果
# e = e // 2
e //= 2
print(e)

# % 取余 取模 模余
f = 3
# f对5来取余数 打印结果
# f = f % 5
f %= 5
print(f)

g = 5
# 求5的3次幂
# g = g ** 3
g **= 3
print(g)
