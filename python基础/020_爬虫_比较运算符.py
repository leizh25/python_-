# 比较运算符返回的都是布尔类型的数据

# 不等于 !=
# 扩展: <>python2版本使用   python3版本使用!=
print(10 != 10)

# == 恒等
print(10 == 10)

# > 大于
print(10 > 20)

# >= 大于等于
print(10 >= 20)

# < 小于
print(10 < 20)

# <= 小于等于
print(10 <= 20)
